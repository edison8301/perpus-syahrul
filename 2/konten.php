<?php 
	if(isset($_GET['page'])){
		$page = $_GET['page'];
		/* |==========================================================| */
		/* |====================| navigasi index |====================| */
		/* |==========================================================| */ 
		switch ($page) {
			case 'home':
				include "home.php";
				break;

			case 'detail_buku':
				include "detail_buku.php";
				break;

			/* |===================================================| */
			/* |====================| Profile |====================| */
			/* |===================================================| */

			case 'edit_profile':
				include "halaman/edit_profile.php";
				break;

			/* |=============================================================| */
			/* |====================| Daftar Peminjaman |====================| */
			/* |=============================================================| */

			case 'daftar_peminjaman_buku':
				include "halaman/mod_peminjaman_buku/daftar_peminjaman_buku.php";
				break;

			/* |================================================================| */
			/* |====================| File Tidak Ditemukan |====================| */
			/* |================================================================| */	
			default:
				include "404.php";
				break;
		}
	}else{
		include "home.php";
	}
 
?>